from __future__ import annotations
import abc

from src.accounts.infra import repository


class AbstractAccountUnitOfWork(abc.ABC):
    accounts: repository.AbstractAccountRepository

    def __enter__(self) -> AbstractAccountUnitOfWork:
        return self

    def __exit__(self, *args):
        self.rollback()

    @abc.abstractmethod
    def commit(self):
        raise NotImplementedError

    @abc.abstractmethod
    def rollback(self):
        raise NotImplementedError


class SqlAccountUnitOfWork(AbstractAccountUnitOfWork):
    def __init__(self, session_factory):
        self.session_factory = session_factory

    def __enter__(self):
        self.session = self.session_factory()
        self.accounts = repository.SqlAccountRepository(self.session)
        return super().__enter__()

    def __exit__(self, *args):
        super().__exit__(*args)
        self.session.close()

    def commit(self):
        self.session.commit()

    def rollback(self):
        self.session.rollback()
